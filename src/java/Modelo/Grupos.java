package Modelo;

/**
 *
 * @author Usuario Local
 */
public class Grupos {
    int id;
    String grupo;

    public Grupos() {
    }

    public Grupos(int id, String grupo) {
        this.id = id;
        this.grupo = grupo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getGrupo() {
        return grupo;
    }

    public void setGrupo(String grupo) {
        this.grupo = grupo;
    }
    
    
}
