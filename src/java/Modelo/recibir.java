package Modelo;

/**
 *
 * @author Usuario Local
 */
public class recibir {

    String in;
    String out;

    public recibir() {
    }

    public recibir(String in, String out) {
        this.in = in;
        this.out = out;
    }

    public String getIn() {
        return in;
    }

    public void setIn(String in) {
        this.in = in;
    }

    public String getOut() {
        return out;
    }

    public void setOut(String out) {
        this.out = out;
    }

}
